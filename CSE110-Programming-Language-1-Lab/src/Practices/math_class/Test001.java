package Practices.math_class;

public class Test001 {

	public static void main(String[] args) {
		
		System.out.println(Math.abs(-7.987));       //absolute value, neg to pos, pos to pos
		
		System.out.println(Math.ceil(9.7));         //the next round figure
		System.out.println(Math.floor(9.7));        //the previous round figure
		
		System.out.println(Math.PI);
		
		System.out.println(Math.max(3.5, 8.4));     //shows max of 2 param
		System.out.println(Math.min(5.6, 2.4));     //shows min of 2 param
		
		System.out.println(Math.pow(6, 2));         //base, power
		
		System.out.println(Math.sqrt(8));           //square root of 8
	}

}
